    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $session_requests = SessionRequest::where('status', 'Not Accepted')->where('to_user_id', Auth::id())->get();
        return view('pages/sessionRequest', compact('session_requests'));
    }

